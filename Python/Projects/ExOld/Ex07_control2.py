# Q5 조건문에 이를 이용해야 하는 문제들을

# 반복에 사용했던 제어변수를 출력 할 수도 있음
n = 0

while(n < 5):
    print(n)
    n += 1
    

# 이 제어변수를 단지 출력하는 것이 아니라, 다른 변수와의 연산에 사용도 가능함
dan = 4
n = 1

while(n < 10):
    print(dan, "x", n, "=", dan * n)
    n += 1

# 조건식에 사용하는 값을 변수가 가진 값으로 지정하는 것도 가능
num = 6
a = 0

while(a < num):
    print("nice")
    a += 1

# ()는 생략가능
a = 0
while a < 10:
    print("good")
    a += 2